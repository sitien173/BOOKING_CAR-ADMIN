    private void updateAvatar() {
        if (fileDialog != null) {
            String url = "/AppUsers/change-avatar";
            MultipartEntityBuilder multipart = MultipartEntityBuilder.create();
            if (fileDialog != null) {
                File file = fileDialog.getSelectedFile();
                multipart.addBinaryBody("avatar", file);
                multipart.addTextBody("username", appUser.getUsername());
            }

            Map<Program.HttpHeader, String> headers = new HashMap<>();
            headers.put(Program.HttpHeader.Authorization, Program.PrefixToken + Program.Token);

            String responseJson = Program.SendHttpFormData(url, Program.HttpMethod.POST, multipart, null, headers);
            Map<String, Object> response = new Gson().fromJson(responseJson, Map.class);
            boolean isSuccess = (boolean) response.get("success");
            if (!isSuccess) {
                String message = response.get("message").toString();
                JOptionPane.showMessageDialog(this, message, "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật ảnh đại diện thành công", "Thông báo", TrayIcon.MessageType.INFO.ordinal(), null);
//                String fileNameUrl = (String) response.get("data");
//                byte[] buff = Program.getFile(fileNameUrl);
//                Icon icon = new ImageIcon(buff);
//                lbIcon.setIcon(icon);
            }
        }
    }
    private void btnChangeAvatarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeAvatarActionPerformed
        if (fileDialog == null) {
            fileDialog = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
            fileDialog.setFileSelectionMode(JFileChooser.FILES_ONLY);
            fileDialog.setMultiSelectionEnabled(false);
            fileDialog.setDialogTitle("Chọn ảnh làm ảnh đại diện");
            FileNameExtensionFilter filter = new FileNameExtensionFilter("images extension support [PNG, JPEG, JPG, Webp, GIF]", "png", "gif", "jpeg", "jpg", "webp");
            fileDialog.addChoosableFileFilter(filter);
        }
        int returnValue = fileDialog.showDialog(null, "Chọn");
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            btnChangeAvatar.setText(fileDialog.getSelectedFile().getName());
        }
    }//GEN-LAST:event_btnChangeAvatarActionPerformed

