    private boolean validateField() {
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email không được để trống", "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
            return false;
        } else {
            // validate
            if (!ValidateUtility.validateEmail(txtEmail.getText())) {
                JOptionPane.showMessageDialog(this, "Email không hợp lệ", "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
                return false;
            }
        }
        if (txtPhoneNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống", "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
            return false;
        }
        if (txtFullName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Họ và tên không được để trống", "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
            return false;
        }
        return true;
    }

    public void saveUser() {
        try {
            String url = "/AppUsers/update";

            Map<String, String> queryParams = new HashMap<>();
            queryParams.put("username", appUser.getUsername());
            Program.HttpMethod method = Program.HttpMethod.PUT;

            AppUserUpdateModel model = new AppUserUpdateModel();
            model.setEmail(txtEmail.getText());
            model.setPhoneNumber(txtPhoneNumber.getText());
            model.setName(txtFullName.getText());

            String jsonPayload = new Gson().toJson(model);
            byte[] data = jsonPayload.getBytes("UTF-8");

            Map<Program.HttpHeader, String> headers = new HashMap<>();
            headers.put(Program.HttpHeader.Authorization, Program.PrefixToken + Program.Token);
            headers.put(Program.HttpHeader.ContentType, "application/json; charset=utf-8");

            String jsonData = Program.SendHttp(url, method, data, queryParams, headers);
            Map<String, Object> response = new Gson().fromJson(jsonData, Map.class);
            boolean isSuccess = (boolean) response.get("success");
            if (!isSuccess) {
                String message = response.get("message").toString();
                JOptionPane.showMessageDialog(this, message, "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công", "Thông báo", TrayIcon.MessageType.INFO.ordinal(), null);
            }
        } catch (ProtocolException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Thông báo", TrayIcon.MessageType.ERROR.ordinal(), null);
        }
    }

